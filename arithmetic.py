def add(num1, num2):
    sum = num1 + num2
    return sum

def subtract(num1, num2):
	difference = num1 - num2
	return difference

def multiply(num1, num2):
	product = num1 * num2
	return product

def divide(num1, num2):
    quotient = float(num1 / num2)
    return quotient

def square(num1):
    squared_number = num1 * num1
    return squared_number

def cube(num1):
    cubed_num = num1 ** 3
    return cubed_num

def power(num1, num2):
    exponents = num1**num2
    return exponents
def mod(num1, num2):
    mod = num1 % num2
    return mod

